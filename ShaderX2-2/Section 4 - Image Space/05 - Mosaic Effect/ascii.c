void display(void)
{
  
  float scaleW, scaleH;


  // Step 1:  Draw the image to the back buffer.
  glViewport (0, 0, width, height);
  glDisable(GL_FRAGMENT_PROGRAM_ARB);
  glDisable(GL_TEXTURE_2D);

  glClearColor(0.0, 0.0, 0.0, 0.0);
  glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

  glMatrixMode(GL_MODELVIEW);
  glLoadIdentity();
  glRotatef(angle, 1.0, 1.0, 1.0);

  // Draw the base image first.  We'll convert it later as a postprocessing step.
  glEnable(GL_LIGHTING);
  glEnable(GL_DEPTH_TEST);
  glColor3f(1.0, 1.0, 0.0);
  glutSolidTeapot(0.5);
  glDisable(GL_LIGHTING);
  glDisable(GL_DEPTH_TEST);
  glFlush();
  
  if (!shouldShade) {
    glutSwapBuffers();
    return;
  }

  // Step 2:  Downsample the image.
  if (glActiveTextureARB) {
    glActiveTextureARB(GL_TEXTURE0_ARB);
  }
  glEnable(GL_TEXTURE_2D);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
  glLoadIdentity();

  // Step 2a:  Copy the screen to a texture.
  glCopyTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, 0, 0, texImageWidth, texImageHeight);

  // Step 2b:  Draw the image back to the screen at half its previous resolution.
  glViewport (0, 0, texImageWidth / 2, texImageHeight / 2);
  glBegin(GL_QUADS);
  glTexCoord2f(0.0, 0.0);
  glVertex2f(-1.0, -1.0);
  glTexCoord2f(1.0, 0.0);
  glVertex2f(1.0, -1.0);
  glTexCoord2f(1.0, 1.0);
  glVertex2f(1.0, 1.0);
  glTexCoord2f(0.0, 1.0);
  glVertex2f(-1.0, 1.0);
  glEnd();

  // Step 3.  Repeat the process to get it to 1/4 its original size.
  glCopyTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, 0, 0, texImageWidth / 2, texImageHeight / 2);
  glViewport (0, 0, texImageWidth / 4, texImageHeight / 4);
  glBegin(GL_QUADS);
  glTexCoord2f(0.0, 0.0);
  glVertex2f(-1.0, -1.0);
  glTexCoord2f(0.5, 0.0);
  glVertex2f(1.0, -1.0);
  glTexCoord2f(0.5, 0.5);
  glVertex2f(1.0, 1.0);
  glTexCoord2f(0.0, 0.5);
  glVertex2f(-1.0, 1.0);
  glEnd();

  // Step 4:  Now 1/8th.
  glCopyTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, 0, 0, texImageWidth / 4, texImageHeight / 4);
  glViewport (0, 0, texImageWidth / 8, texImageHeight / 8);
  glBegin(GL_QUADS);
  glTexCoord2f(0.0, 0.0);
  glVertex2f(-1.0, -1.0);
  glTexCoord2f(0.25, 0.0);
  glVertex2f(1.0, -1.0);
  glTexCoord2f(0.25, 0.25);
  glVertex2f(1.0, 1.0);
  glTexCoord2f(0.0, 0.25);
  glVertex2f(-1.0, 1.0);
  glEnd();

  // Now perform one more pass to scale the image.  We have to change the aspect ratio
  // of the downsampled image to match the aspect ratio of the font, so each block of
  // character in the font matches the same color in the downsampled image.
  // The font is 8 x 10 pixels.  This means that we have to scale the Y-direction of the 
  // rendering somewhat so that 10 pixels in height correspond to 1 pixel in this final texture image.
  glCopyTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, 0, 0, texImageWidth / 8, texImageHeight / 8);
  glViewport (0, 0, texImageWidth / 8, texImageHeight / 8 / 1.25);
  glBegin(GL_QUADS);
  glTexCoord2f(0.0, 0.0);
  glVertex2f(-1.0, -1.0);
  glTexCoord2f(0.125, 0.0);
  glVertex2f(1.0, -1.0);
  glTexCoord2f(0.125, 0.125);
  glVertex2f(1.0, 1.0);
  glTexCoord2f(0.0, 0.125);
  glVertex2f(-1.0, 1.0);
  glEnd();

  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);

  glCopyTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, 0, 0, texImageWidth / 8, texImageHeight / 8);
  glViewport (0, 0, texImageWidth, texImageHeight);
  glBegin(GL_QUADS);
  glTexCoord2f(0.0, 0.0);
  glVertex2f(-1.0, -1.0);
  glTexCoord2f(0.125, 0.0);
  glVertex2f(1.0, -1.0);
  glTexCoord2f(0.125, 0.125/1.25);
  glVertex2f(1.0, 1.0);
  glTexCoord2f(0.0, 0.125/1.25);
  glVertex2f(-1.0, 1.0);
  glEnd();
 
  // Copy the final downsampled image into our texture.
  glCopyTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, 0, 0, texImageWidth, texImageHeight);

  // These scaling factors are used to match texels in the downsampled image with 
  // pixels in the original screen image.
  scaleW = 1.0 * width / texImageWidth;
  scaleH = 1.0 * height / texImageHeight;

  // Now activate the fragment program and re-render the scene using the original image
  // as a texture source.
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
  if (!stopAtDownsample && fpSupported) {
    glEnable(GL_FRAGMENT_PROGRAM_ARB);
    glViewport (0, 0, width, height);
    glBegin(GL_QUADS);
    glTexCoord2f(0.0, 0.0);
    glMultiTexCoord2fARB(GL_TEXTURE1_ARB, 0.0, 0.0);
    glVertex2f(-1.0, -1.0);
    glTexCoord2f(scaleW, 0.0);
    glMultiTexCoord2fARB(GL_TEXTURE1_ARB, 1.0 * width / 8.0, 0.0);
    glVertex2f(1.0, -1.0);
    glTexCoord2f(scaleW, scaleH);
    glMultiTexCoord2fARB(GL_TEXTURE1_ARB, 1.0 * width / 8.0, 1.0 * height / 10.0);
    glVertex2f(1.0, 1.0);
    glTexCoord2f(0.0, scaleH);
    glMultiTexCoord2fARB(GL_TEXTURE1_ARB, 0.0, 1.0 * height / 10.0);
    glVertex2f(-1.0, 1.0);
    glEnd();
    glDisable(GL_FRAGMENT_PROGRAM_ARB);
  }

  // Display the final image.
  if (!fpSupported) {
    glDisable(GL_TEXTURE_2D);
    glutRenderChars(-1.0, 0.95, "Error:  Your graphics hardware does not support");
    glutRenderChars(-1.0, 0.90, "GL_ARB_fragment_program, a required extension for this sample.");
  }
  glutSwapBuffers();

}

